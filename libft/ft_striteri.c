#include "libft.h"
#include <stddef.h>

void	ft_striteri(char const *s, void (*f)(unsigned int, char*))
{
	size_t	i;
	size_t	len;

	len = ft_strlen(s);
	i = 0;
	if (!s || !f)
		return ;
	while (i < len)
	{
		(*f)(i, (char *)s);
		i++;
		s++;
	}
}
